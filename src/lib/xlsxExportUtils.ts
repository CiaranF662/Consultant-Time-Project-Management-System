import * as XLSX from 'xlsx'
import formatDate, { formatDateTime } from '@/lib/formatDate';

export interface ExportData {
  sheetName: string
  data: Array<Record<string, any>>
}

export const generateXLSXReport = (reportData: ExportData[], filename: string): void => {
  const workbook = XLSX.utils.book_new()
  
  reportData.forEach((sheet) => {
    const worksheet = XLSX.utils.json_to_sheet(sheet.data)
    
    // Auto-size columns
    const range = XLSX.utils.decode_range(worksheet['!ref'] || 'A1')
    const colWidths: number[] = []
    
    for (let col = range.s.c; col <= range.e.c; col++) {
      let maxWidth = 10
      for (let row = range.s.r; row <= range.e.r; row++) {
        const cellAddress = XLSX.utils.encode_cell({ r: row, c: col })
        const cell = worksheet[cellAddress]
        if (cell && cell.v) {
          const cellLength = cell.v.toString().length
          maxWidth = Math.max(maxWidth, cellLength + 2)
        }
      }
      colWidths[col] = Math.min(maxWidth, 50) // Cap at 50 characters
    }
    
    worksheet['!cols'] = colWidths.map(width => ({ width }))
    
    XLSX.utils.book_append_sheet(workbook, worksheet, sheet.sheetName)
  })
  
  // Add metadata sheet
  const metadata = [
    { Property: 'Generated On', Value: formatDateTime(new Date()) },
    { Property: 'Generated By', Value: 'Jira Insight Platform' },
    { Property: 'Report Type', Value: 'Resource Strategy Analysis' }
  ]
  const metaSheet = XLSX.utils.json_to_sheet(metadata)
  XLSX.utils.book_append_sheet(workbook, metaSheet, 'Metadata')
  
  // Download
  const finalFilename = `${filename}_${new Date().toISOString().split('T')[0]}.xlsx`
  XLSX.writeFile(workbook, finalFilename)
}

export const generateGrowthTeamXLSX = (data: {
  overview: any[]
  profitability: any[]
  utilization: any[]
  projects: any[]
}): void => {
  const exportData: ExportData[] = [
    {
      sheetName: 'Overview',
      data: data.overview
    },
    {
      sheetName: 'Client Profitability',
      data: data.profitability
    },
    {
      sheetName: 'Resource Utilization',
      data: data.utilization
    },
    {
      sheetName: 'Project Portfolio',
      data: data.projects
    }
  ]
  
  generateXLSXReport(exportData, 'growth_team_report')
}

