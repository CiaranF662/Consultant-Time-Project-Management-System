export const generateAllocationXLSX = (
  allocations: any[],
  consultants: any[],
  projects: any[]
): void => {
  // Prepare allocation data with joined information
  const allocationData = allocations.map(allocation => {
    const consultant = consultants.find(c => c.id === allocation.consultantId)
    const project = projects.find(p => p.id === allocation.projectId)
    
    return {
      'Consultant': consultant?.name || 'Unknown',
      'Project': project?.name || 'Unknown Project',
      'Client': project?.clientName || 'Unknown Client',
      'Role': allocation.roleInProject || 'Consultant',
      'Hours per Week': allocation.hoursPerWeek,
  'Start Date': formatDate(allocation.startDate),
  'End Date': formatDate(allocation.endDate),
      'Status': allocation.status,
      'Hourly Rate': consultant?.hourlyRate ? `$${consultant.hourlyRate}` : 'N/A',
      'Weekly Cost': consultant?.hourlyRate 
        ? `$${(consultant.hourlyRate * allocation.hoursPerWeek).toFixed(2)}` 
        : 'N/A'
    }
  })
  
  // Prepare consultant utilization summary
  const consultantSummary = consultants.map(consultant => {
    const consultantAllocations = allocations.filter(a => a.consultantId === consultant.id)
    const totalHours = consultantAllocations.reduce((sum, a) => sum + a.hoursPerWeek, 0)
    const utilization = ((totalHours / 40) * 100).toFixed(1) // Assuming 40h work week
    
    return {
      'Consultant': consultant.name,
      'Role': consultant.role,
      'Department': consultant.department || 'N/A',
      'Skills': consultant.skills || 'N/A',
      'Hourly Rate': consultant.hourlyRate ? `$${consultant.hourlyRate}` : 'N/A',
      'Total Allocated Hours': totalHours,
      'Utilization %': `${utilization}%`,
      'Active Projects': consultantAllocations.length,
      'Status': totalHours > 40 ? 'Overallocated' : totalHours < 32 ? 'Underutilized' : 'Optimal'
    }
  })
  
  // Prepare project summary
  const projectSummary = projects.map(project => {
    const projectAllocations = allocations.filter(a => a.projectId === project.id)
    const totalHours = projectAllocations.reduce((sum, a) => sum + a.hoursPerWeek, 0)
    const teamSize = projectAllocations.length
    
    return {
      'Project': project.name,
      'Client': project.clientName || 'Unknown Client',
      'Status': project.status,
      'Priority': project.priority,
      'Budget': project.budget ? `$${project.budget.toLocaleString()}` : 'N/A',
  'Start Date': formatDate(project.startDate),
  'End Date': formatDate(project.endDate),
      'Progress %': `${(project.progress * 100).toFixed(1)}%`,
      'Team Size': teamSize,
      'Total Weekly Hours': totalHours,
      'Jira Project': project.jiraProjectKey || 'Not Linked'
    }
  })
  
  const exportData: ExportData[] = [
    {
      sheetName: 'Resource Allocations',
      data: allocationData
    },
    {
      sheetName: 'Consultant Summary',
      data: consultantSummary
    },
    {
      sheetName: 'Project Summary',
      data: projectSummary
    }
  ]
  
  generateXLSXReport(exportData, 'resource_allocation_matrix')
}